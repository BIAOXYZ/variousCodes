class Solution(object):
    def findLength(self, A, B):
        """
        :type A: List[int]
        :type B: List[int]
        :rtype: int
        """

        """
        思想：
        最基本的想法就是先做一遍循环把每个数字的index弄成一个类似{number:[ind1, ind2,...]}
        的字典。然后遍历对比，但是时间复杂度好像是O(n^4)，估计大概率超时。
        """

        lena, lenb = len(A), len(B)
        dicb = dict()
        for i in range(lenb):
            if dicb.has_key(B[i]):
                dicb[B[i]].append(i)
            else:
                dicb[B[i]] = [i]
        
        maxlongest = 0
        for i in range(lena):
            # 可能的实际下标都大于等于边界了，那就不可能有更长的了，
            # 所以此时可以直接返回当前的maxlongest。
            if i + maxlongest - 1 >= lena - 1:
                return maxlongest
            if not dicb.has_key(A[i]):
                continue
            indexarr = dicb[A[i]]
            for ii in indexarr:
                # 这里按理说该用continue，但实际上如果某个ii不合法，后面的ii只会更大，所以直接break了。
                if ii + maxlongest - 1 >= lenb - 1:
                    break
                currlongest = 1
                # 因为下面紧接着还要加上1，所以while里面的两个小于号不能取<=
                while i + currlongest - 1 < lena - 1 and ii + currlongest - 1 < lenb - 1:
                    currlongest += 1
                    if A[i + currlongest - 1] != B[ii + currlongest - 1]:
                        currlongest -= 1
                        break
                maxlongest = max(maxlongest, currlongest) 
        return maxlongest
        
